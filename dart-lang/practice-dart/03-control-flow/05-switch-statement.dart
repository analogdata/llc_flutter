void main() {

  // Switch Statment: Switch can be used to replace multiple if-else conditions
  const pos = 1;
  switch (pos) {
    case 1:
      print("Gold Medal");
      break;
    case 2:
      print("Silver Medal");
      break;
    case 3:
      print("Bronze Medal");
      break;
    default:
      print("No Medal");
      break;
  }
}